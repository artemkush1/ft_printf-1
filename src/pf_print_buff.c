#include "ft_printf.h"

void	pf_print_buff(t_print *ptr)
{
	if (ptr->bp_i > 0)
	{
		write(ptr->fd, ptr->bp, ptr->bp_i);
		ptr->res += ptr->bp_i;
	}
	ptr->bp_i = 0;
}
